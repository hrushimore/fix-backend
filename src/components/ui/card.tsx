import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { withGlow?: boolean }
>(({ className, withGlow = true, ...props }, ref) => (
  <div className={cn("relative group", !withGlow && "pointer-events-none")}>
    {withGlow && (
      <div className="absolute -inset-0.5 bg-gradient-to-r from-blue-500 via-purple-500 to-pink-500 rounded-xl opacity-0 group-hover:opacity-70 blur-md transition duration-1000 group-hover:duration-200 dark:opacity-0" />
    )}
    <div
      ref={ref}
      className={cn(
        "relative rounded-lg border bg-card text-card-foreground shadow-sm transition-all duration-300",
        withGlow && "group-hover:shadow-glow dark:group-hover:shadow-glow",
        className
      )}
      {...props}
    />
  </div>
))
Card.displayName = "Card"

